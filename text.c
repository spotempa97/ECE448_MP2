/*      text_buffer holds the intutive (x,y) coordinates of the buffer
*       plane0, plane1, plane2, and plane3 contain the actual plane to be drawn to video memory. 
*       planes points to each of the four planes
*/
unsigned char text_buffer[STATUS_BAR_WIDTH][STATUS_BAR_HEIGHT];
unsigned char plane0[PLANE_SIZE];
unsigned char plane1[PLANE_SIZE];
unsigned char plane2[PLANE_SIZE];
unsigned char plane3[PLANE_SIZE];
unsigned char* planes[NUM_OF_PLANES];

/* keeps track of if the buffer has been filled yet to prevent segmentation fault. */
int buff_filled = 0;

/*
 * text_to_graphics
 *     DESCRIPTION: Place the status bar into the buffer by first translating bitwise information on each letter
 *                  and placing it into an intuitive location.  Then translate this information into buffers readable
 *                  by the VGA video memory.
 *     INPUTS:      left_string should be drawn to the left of the video screen.
 *                  middle_string should be drawn to the middle of the video screen.
 *                  right_string should be drawn to the right of the video screen.
 *     OUTPUTS:     modifies plane buffers for status bar
 *     RETURN VALUE: none
 *     NOTE:        Only the middle-string or the (left and right strings) should contain data.  All others should be NULL
 */
void text_to_graphics(const char* left_string, const char* middle_string, const char* right_string) {
    int i, j, k;
    int bitmask;
    int leftString_size, middleString_size, rightString_size;

    buff_filled = 1;

    /* If middle string is nonzero, we are drawing an interrupt and can ignore left and right. 
     * Otherwise we only care about left and right (room name and user input)
     */
    if(middle_string != NULL) {
        // set string sizes appropriately
       middleString_size = strlen(middle_string);

       /* Determine starting and ending position for the middle string. */
       int middle_start_point = ((MAX_NUMBER_OF_CHARS / 2) -  (middleString_size / 2));
       int middle_end_point = middle_start_point + middleString_size;

        /* Set buffer for MIDDLE region where text appears. */
        for(i = middle_start_point; i < middle_end_point; i++) {
            for(j = 1; j <= FONT_HEIGHT; j++) {
                bitmask = 128;                      // bitmask starts at top bit and is bitshift right down to 1 for each horizontal line per char.
                for(k = 0; k < FONT_WIDTH; k++) {
                    text_buffer[k + (i * FONT_WIDTH)][j] = (bitmask & (font_data[(int)middle_string[i - middle_start_point]][j - 1]));
                    bitmask = bitmask >> 1;
                    if(text_buffer[k + (i * FONT_WIDTH)][j] != 0) {
                        text_buffer[k + (i * FONT_WIDTH)][j] = TEXT_COLOR;
                    }
                    else {
                        text_buffer[k + (i * FONT_WIDTH)][j] = STATUS_BAR_COLOR;
                    }
                }
            }
        }

        /* Set buffer for excess regions where no chars were written on the left of the status bar. */
        for(i = 0; i < (middle_start_point * FONT_WIDTH); i++) {
            for(j = 1; j <= FONT_HEIGHT; j++) {
                text_buffer[i][j] = STATUS_BAR_COLOR;
            }
        }
        /* Set buffer for excess regions where no chars were written on the right of the status bar. */
        for(i = STATUS_BAR_WIDTH; i > (middle_end_point * FONT_WIDTH); i--) {
            for(j = 1; j <= FONT_HEIGHT; j++) {
                text_buffer[i][j] = STATUS_BAR_COLOR;
            }
        }
    }
    else {
        // set string sizes appropriately
        leftString_size = strlen(left_string);
        rightString_size = strlen(right_string);

        /* Set buffer for LEFT region where text appears. */
        for(i = 0; i < leftString_size; i++) {
            for(j = 1; j <= FONT_HEIGHT; j++) {
                bitmask = 128;                      // bitmask starts at top bit and is bitshift right down to 1 for each horizontal line per char.
                for(k = 0; k < FONT_WIDTH; k++) {
                    text_buffer[k + (i * FONT_WIDTH)][j] = (bitmask & (font_data[(int)left_string[i]][j - 1]));
                    bitmask = bitmask >> 1;
                    if(text_buffer[k + (i * FONT_WIDTH)][j] != 0) {
                        text_buffer[k + (i * FONT_WIDTH)][j] = TEXT_COLOR;
                    }
                    else {
                        text_buffer[k + (i * FONT_WIDTH)][j] = STATUS_BAR_COLOR;
                    }
                }
            }
        }

        /* Set buffer for RIGHT region where text appears. */
        for(i = (MAX_NUMBER_OF_CHARS - rightString_size); i < MAX_NUMBER_OF_CHARS; i++) {
            for(j = 1; j <= FONT_HEIGHT; j++) {
                bitmask = 128;                      // bitmask starts at top bit and is bitshift right down to 1 for each horizontal line per char.
                for(k = 0; k < FONT_WIDTH; k++) {
                    text_buffer[k + (i * FONT_WIDTH)][j] = (bitmask & (font_data[(int)right_string[(i + rightString_size - MAX_NUMBER_OF_CHARS)]][j - 1]));
                    bitmask = bitmask >> 1;
                    if(text_buffer[k + (i * FONT_WIDTH)][j] != 0) {
                        text_buffer[k + (i * FONT_WIDTH)][j] = TEXT_COLOR;
                    }
                    else {
                        text_buffer[k + (i * FONT_WIDTH)][j] = STATUS_BAR_COLOR;
                    }
                }
            }
        }
        /* Set buffer for excess regions where no chars were written in the middle of the status bar. */
        for(i = leftString_size * FONT_WIDTH; i < (STATUS_BAR_WIDTH) - (rightString_size * FONT_WIDTH); i++) {
            for(j = 1; j <= FONT_HEIGHT; j++) {
                text_buffer[i][j] = STATUS_BAR_COLOR;
            }
        }
    }



    /* Set buffer for top and bottom lines to the status bar color (default). */
    for(i = 0; i < STATUS_BAR_WIDTH; i++) {
        text_buffer[i][0] = STATUS_BAR_COLOR;
        text_buffer[i][17] = STATUS_BAR_COLOR;
    }

    /* Set text_buffer data into 4 planes. */
    for(i = 0; i < STATUS_BAR_WIDTH / 4; i++) {
        for(j = 0; j < STATUS_BAR_HEIGHT; j++) {
            plane0[i + (j * (STATUS_BAR_WIDTH / 4))] = text_buffer[4*i][j];
            plane1[i + (j * (STATUS_BAR_WIDTH / 4))] = text_buffer[4*i+1][j];
            plane2[i + (j * (STATUS_BAR_WIDTH / 4))] = text_buffer[4*i+2][j];
            plane3[i + (j * (STATUS_BAR_WIDTH / 4))] = text_buffer[4*i+3][j];
        }
    }

    /* Set planes to contain data for each buffer based on the offset. */
    planes[0] = plane0;
    planes[1] = plane1;
    planes[2] = plane2;
    planes[3] = plane3;
}

unsigned char* get_textBuffer(int offset) {
    /* if the buffer has not been filled yet, fill it with a blank screen to prevent segFaults by drawing and invalid buffer */
    if(!buff_filled) {
        //text_to_graphics("");
        text_to_graphics("","","");
    }
    return planes[offset];
}

